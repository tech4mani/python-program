# #ADD()
a={"mani","siva","arun"}
a.add("kavi")
print(a)

#remove
a={"mani","siva","arun"}
a.remove("arun")
print(a)

#discard()
a={"mani","siva","arun"}
a.discard("arun")
print(a)

#union()
a={"mani","siva","arun"}
b={"hari","raj"}
c=a.union(b)
print(c)

# #insert()
a=[12,8,7,6,3]
a.insert(3,11)
print(a)